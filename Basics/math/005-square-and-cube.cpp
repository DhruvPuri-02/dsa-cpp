// Problem  : Square and Cube of a Number
// Topic    : Basics / Math
// Approach : Use arithmetic operators to compute square and cube

#include <iostream>
using namespace std;

int main() {
    int n;
    cin >> n;

    cout << "Square : " << n * n << endl;
    cout << "Cube : " << n * n * n;

    return 0;
}
